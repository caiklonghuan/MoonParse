#!/usr/bin/env node
/**
 * Precompile all built-in grammar presets into parse-table JSON.
 *
 * Run:  node scripts/precompile-grammars.mjs
 *
 * Output: src/data/precompiledTables.js
 *         (an ES module mapping preset id → table JSON string)
 */

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')

// ---------- load WASM wrapper ----------

const wasmPath = resolve(ROOT, 'public/moonparse.wasm')
const bytes = readFileSync(wasmPath)
const mod = await WebAssembly.compile(bytes, { builtins: ['js-string'] })
const importObj = { '_': new Proxy({}, { get(_, name) { return name } }) }
const { exports: wasm } = await WebAssembly.instantiate(mod, importObj)

function createParser(dsl) {
  const pid = wasm.parser_create_from_dsl(dsl)
  if (pid < 0) throw new Error('createParser failed')
  return pid
}

function tableJson(pid) {
  return wasm.parser_table_to_json(pid) ?? ''
}

function freeParser(pid) {
  wasm.parser_free(pid)
}

// ---------- import presets (dynamic import of ES module) ----------

import { pathToFileURL } from 'node:url'
const presetsPath = resolve(ROOT, 'src/data/languagePresets.js')
const { BUILTIN_LANGUAGE_PRESETS, EXAMPLE_PRESETS } = await import(pathToFileURL(presetsPath).href)

// ---------- precompile each preset ----------

const tables = {}
let total = 0
const allPresets = EXAMPLE_PRESETS ?? BUILTIN_LANGUAGE_PRESETS

for (const preset of allPresets) {
  const t0 = performance.now()
  try {
    const pid = createParser(preset.grammar)
    const json = tableJson(pid)
    freeParser(pid)
    tables[preset.id] = json
    const ms = (performance.now() - t0).toFixed(1)
    const kb = (json.length / 1024).toFixed(1)
    console.log(`  ✓ ${preset.id.padEnd(12)} ${ms.padStart(7)}ms  ${kb.padStart(7)}KB`)
    total++
  } catch (e) {
    console.error(`  ✗ ${preset.id}: ${e.message}`)
  }
}

// ---------- write output ----------

const outPath = resolve(ROOT, 'src/data/precompiledTables.js')

let code = '// Auto-generated by scripts/precompile-grammars.mjs\n'
code += '// Do not edit manually.\n\n'
code += 'export const PRECOMPILED_TABLES = {\n'

for (const [id, json] of Object.entries(tables)) {
  // Store as a plain string literal. Use JSON.stringify to escape properly.
  code += `  ${JSON.stringify(id)}: ${JSON.stringify(json)},\n`
}

code += '}\n'

writeFileSync(outPath, code, 'utf-8')
const outKB = (code.length / 1024).toFixed(1)
console.log(`\n✓ ${total} grammars precompiled → ${outPath} (${outKB}KB)`)
